    private void savebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtnActionPerformed
        CarModel c = new CarModel();
        
        c.setPlateNo(platetxt.getText());
        c.setModel(modeltxt.getText());
        c.setType(typetxt.getText());
        c.setPrice(Double.parseDouble(pricetxt.getText()));
        
        
        ccont.CControl(c);
        
        DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
        model.insertRow(model.getRowCount(), new Object[]{platetxt.getText(), modeltxt.getText(), typetxt.getText(),
        pricetxt.getText(),ownercombo.getSelectedItem()
       
            });
       
        calculate();
        combobx();
    }//GEN-LAST:event_savebtnActionPerformed

