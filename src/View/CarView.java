    public void combobx(){
        OwnerModel cbx = new OwnerModel();
        
        Integer IdNumber = Integer.parseInt((String) ownercombo.getSelectedItem());
        
        String Owner_name= cbx.getFirstName()  +  cbx.getLastName();
        String Owner_contact=cbx.getPhoneNumber();
        
        ownernametxt.setText(Owner_name+"");
        ownercontacttxt.setText(Owner_contact+"");
    }
    
    public void calculate(){
        Double price = Double.parseDouble(pricetxt.getText());
        
        Double tax = (price*60)/100;
        Double net = price + tax;
        
        taxtxt.setText(tax +"");
        nettxt.setText(net +"");
    }
    
